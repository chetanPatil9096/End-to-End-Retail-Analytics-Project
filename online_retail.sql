#Query 2 — Top Countries by Revenue
#Identify the top 10 countries generating the highest revenue.

SELECT 
    Country,
    ROUND(SUM(Revenue), 2) AS Revenue
FROM retail_sales
GROUP BY Country
ORDER BY Revenue DESC
