#Query 23 — Repeat Customers
#Identify customers who placed more than one order.

SELECT 
    `Customer ID`,
    
    COUNT(DISTINCT Invoice) AS Total_Orders
    
FROM retail_sales

GROUP BY `Customer ID`

HAVING COUNT(DISTINCT Invoice) > 1

