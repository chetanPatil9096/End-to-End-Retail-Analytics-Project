#Query 3 — Monthly Revenue Trend
#Analyze monthly revenue trends across different years.

SELECT 
    Year,
    Month,
    ROUND(SUM(Revenue), 2) AS Monthly_Revenue
FROM retail_sales
GROUP BY Year, Month
