#Query 26 — Top 5 Customers from Each Country
#Identify the top 5 highest spending customers from each country.

WITH customer_revenue AS (
    
    SELECT
        Country,
        `Customer ID`,
        ROUND(SUM(Revenue), 2) AS Total_Revenue,
        
        DENSE_RANK() OVER (
            PARTITION BY Country
            ORDER BY SUM(Revenue) DESC
        ) AS customer_rank
        
    FROM retail_sales
    
    GROUP BY Country, `Customer ID`
)

SELECT *
FROM customer_revenue
WHERE customer_rank <= 5;


#Query 27 — Revenue Category Classification
#Classify customers into revenue categories based on total spending.

SELECT 
    `Customer ID`,
    
    ROUND(SUM(Revenue), 2) AS Total_Spending,
    
    CASE
    
        WHEN SUM(Revenue) >= 10000
        THEN 'High Value'
        
        WHEN SUM(Revenue) >= 5000
        THEN 'Medium Value'
        
        ELSE 'Low Value'
        
    END AS Customer_Category
    
FROM retail_sales

GROUP BY `Customer ID`

ORDER BY Total_Spending DESC;


#Query 28 — Most Frequently Purchased Products
#Find products purchased most frequently across transactions.

SELECT 
    Description,
    
    COUNT(*) AS Purchase_Frequency
    
FROM retail_sales

GROUP BY Description

ORDER BY Purchase_Frequency DESC
LIMIT 10;


