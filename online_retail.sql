#Query 16 — Rank Customers by Revenue
#Rank customers based on total revenue generated.

SELECT 
    `Customer ID`,
    ROUND(SUM(Revenue), 2) AS Total_Revenue,
    RANK() OVER (
        ORDER BY SUM(Revenue) DESC
    ) AS Customer_Rank
FROM retail_sales
GROUP BY `Customer ID`;


#Query 17 — Running Monthly Revenue
#Calculate cumulative monthly revenue over time.

SELECT 
    Year,
    Month,
    ROUND(SUM(Revenue), 2) AS Monthly_Revenue,
    
    ROUND(
        SUM(SUM(Revenue)) OVER (
            ORDER BY Year, Month
        ), 2
    ) AS Running_Revenue
FROM retail_sales
GROUP BY Year, Month;

#Query 18 — Revenue Contribution Percentage
#Calculate each country's percentage contribution to total revenue.

SELECT 
    Country,
    
    ROUND(SUM(Revenue), 2) AS Country_Revenue,
    
    ROUND(
        (SUM(Revenue) / 
        (SELECT SUM(Revenue) FROM retail_sales)) * 100,
        2
    ) AS Revenue_Percentage
FROM retail_sales
GROUP BY Country
ORDER BY Revenue_Percentage DESC;

#Query 19 — Top Product per Country
#Identify the highest-selling product in each country.

WITH product_sales AS (
    
    SELECT
        Country,
        Description,
        SUM(Quantity) AS Total_Quantity,
        
        RANK() OVER (
            PARTITION BY Country
            ORDER BY SUM(Quantity) DESC
        ) AS product_rank
        
    FROM retail_sales
    
    GROUP BY Country, Description
)

SELECT *
FROM product_sales
WHERE product_rank = 1;


#Query 20 — High Value Customers
#Identify customers whose spending is higher than average customer spending.

SELECT 
    `Customer ID`,
    ROUND(SUM(Revenue), 2) AS Total_Spending
FROM retail_sales
GROUP BY `Customer ID`
HAVING SUM(Revenue) >
(
    SELECT AVG(customer_total)
    FROM (
        SELECT SUM(Revenue) AS customer_total
        FROM retail_sales
        GROUP BY `Customer ID`
    ) AS avg_table
)
ORDER BY Total_Spending DESC;



