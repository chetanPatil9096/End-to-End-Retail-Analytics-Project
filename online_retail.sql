#Query 24 — Monthly Growth Rate
#Calculate month-over-month revenue growth rate.

WITH monthly_revenue AS (
    
    SELECT
        Year,
        Month,
        SUM(Revenue) AS Revenue
        
    FROM retail_sales
    
    GROUP BY Year, Month
)

SELECT
    Year,
    Month,
    ROUND(Revenue, 2) AS Revenue,
    
    ROUND(
        (
            Revenue -
            LAG(Revenue) OVER (
                ORDER BY Year, Month
            )
        )
        /
        LAG(Revenue) OVER (
            ORDER BY Year, Month
        ) * 100,
        2
    ) AS Growth_Rate_Percentage
    
FROM monthly_revenue;


#Query 25 — Customer Lifetime Value (CLV)
#Calculate customer lifetime value based on total spending.

SELECT 
